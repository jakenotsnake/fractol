#include "libft.h"
#include "string.h"

char	*ft_strchr(const char *s, int c)
{
	if (c > 256)
	{
		c = (c - 256);
	}
	while ((*s != '\0') && (*s != c))
	{
		s++;
	}
	if (*s == c)
	{
		return ((char *)s);
	}
	return ((char *) NULL);
}
